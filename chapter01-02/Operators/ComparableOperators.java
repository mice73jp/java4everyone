public class ComparableOperators {
    public static void main(String[] args) {
        int i = 10;
        int j = 10;

        System.out.printf("i == j => %b\n", i == j);
        System.out.printf("i != j => %b\n", i != j);
        System.out.printf("i > j => %b\n", i > j);
        System.out.printf("i < j => %b\n", i < j);
        System.out.printf("i >= j => %b\n", i >= j);
        System.out.printf("i <= j => %b\n", i <= j);
    }
}
